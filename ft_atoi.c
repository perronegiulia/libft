int	ft_atoi(const char *str)
{
	int	i;
	int	p;
	int	nb;

	i = 0;
	p = 1;
	nb = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		p = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str [i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (p * nb);
}
